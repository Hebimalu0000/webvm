<p>WebVM は、WebAssembly を介してブラウザ上で実行される仮想 Linux 環境です。</p>
<p>ベースとなったもの:</p>
<ul class="list-disc list-inside">
	<li><a class="underline" target="_blank" href="https://cheerpx.io/">CheerpX</a>: Wasm の x86 JIT</li>
	<li><a class="underline" target="_blank" href="https://xtermjs.org/">Xterm.js</a>: 対話型ターミナル</li>
	<li>ローカル/プライベート <a class="underline" target="_blank" href="https://cheerpx.io/docs/guides/File-System-support">ファイルストレージ</a></li>
	<li><a class="underline" target="_blank" href="https://cheerpx.io/docs/guides/Networking">Networking</a> の経由システム: <a class="underline" target="_blank" href="https://tailscale.com/">Tailscale</a></li>
</ul>
